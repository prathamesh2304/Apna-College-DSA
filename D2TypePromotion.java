public class D2TypePromotion {
    public static void main(String[] args) {
        char a = 'a';
        char b = 'b';
        int ans = (b - a);
        System.out.print((int)b);
        System.out.print((int)a);
        System.out.println(ans);

        float f = 20.34f;
        double d = 80;
        int i = 20;
        double answer = f + d + i;
        System.out.println(answer);
    }
}
